                <p>Vapormax 打破桎梏的关键，用耐克创新中心副总裁 Kathy Gomez 的话说，就是：“我们意识到空气并不是越多越好，而是应该用在恰到好处的地方。”</p>

                <p>换句话说，耐克设计团队不仅要解决气囊的耐磨性问题，还需要解决灵敏度问题。气囊给使用者的双脚提供了保护，但脚的各个位置施力程度不同，原始的全掌气垫很难给予准确的回应。在过去 29
                    年里，气囊仅仅作为缓冲技术而存在，如今它需要转变角色，变成鞋底本身。</p>

                <p>“在我们做过 50 双左右的原型鞋里，有一款非常接近 Vapormax，它几乎解决了气囊分布的问题，可以很好地分解压力。”Kathy Gomez
                    对《好奇心日报》说。因为研发地在美国科罗拉多，这双原型鞋的代号就变成了“科罗拉多”。</p>

                <p>Vapormax 设计团队大约由 25 至 30 人构成，整个研发时间耗时 7 年。按照耐克跑步鞋类副总裁 Brett Holts
                    的说法，“比大多数产品的时间都要久。”事后描述整个设计思路似乎很简单：研究如何去掉外底，研究如何让脚接触气囊，研究如何去掉所有不必要的结构和层次。它最终的目标，是“绝对的贴合”。</p>

