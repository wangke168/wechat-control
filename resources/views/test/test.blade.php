                <p class="">耐克气垫球鞋最早出现在 1979 年，而技术本身的时间还要往前推 2 年。一个叫 Marion Frank Rudy
                    的发明家希望在球鞋里塞入气囊，这样不仅可以提供缓震效果，还可以减轻球鞋的重量。这个想法被当时的运动市场主导者阿迪达斯拒之门外，事实上，就连鲍尔曼教练也觉得这是一个注定要失败的想法。但是耐克的创始人
                    Phil Knight 喜欢这个主意，它在种种试验之后被第一次应用到了叫 Tailwind 的球鞋里。<br></p>

                <p class="">1987 年，可视气垫系列 Air Max
                    诞生，随后成为耐克公司销售额快速增长的产品线，每年的销售额超过十亿美元——追捧者认为，这种裸露产品技术元件的做法有别于当时流行的“形式追随功能”，几乎算得上一种新符号学。无数人就此爱上了这种“对新技术的炫耀性展示”，因为该产品过于成功，决定让气垫直接显露出来的两位设计师
                    Tinker Hatfield 和 Mark
                    Parker（当时他还是鞋类研发部门主管）一度被误认为是耐克气垫系列的开创者。其实两人当时已经对气垫技术有了相当的了解，后者还为耐克创立了气垫创新研发部门。</p>

                <p class="">耐克如今每年生产 1.7 亿双各种各样的气垫，产地均在美国。不过就 Air Max 本身而言，产品创新已经很久没有突破了。Air Max 长达 29
                    年的产品迭代过程，简单解释起来就是“每一代产品都比上一代容纳更多气体”。耐克缓震创新总监 David Forland 的说法是：“发泡材料会破损，但空气不会。”</p>

