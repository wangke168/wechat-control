                <p class="">Vapormax 第一次亮相是在 2016 年 3 月耐克纽约创新大会上。耐克 CEO Mark Parker
                    在简短发言里提及：耐克第一次去掉球鞋的中底，不适用橡胶，重新定义了球鞋的审美和制造方式。不过当时的媒体把大多数注意力都放在可以自动系鞋带的 hyperadapt 1.0 身上，那双蓝色
                    Vapormax 的展位并不热闹。</p>

                <p class="">
                    将近一年之后，耐克改用灰色鞋面和全透明鞋底向媒体展示这项新产品。在几乎全白的背景墙下，这种颜色组合反倒有一种意外的纯净效果，也削弱了气囊鞋底带来的过于强烈的科技感。和耐克历史上所有的气垫产品相比，Vapormax
                    的确太前卫了。</p>

