                <div class="com-insert-images"></div>
                <p class="">而之所以耗时 7 年，是因为耐克不仅要设计 Vapormax
                    这个产品，还需要设计让它量产的方式。对于任何无先例可循的工业产品来说，设计生产机器比设计产品本身难度更高、也更耗费时间。在这一点上，从福特的 T 型车到苹果公司的 iPhone 手机均是如此。<br>
                </p>

                <div class="com-insert-images"></div>
                <p class=""></p>

                <p></p>

                <p>Vapormax 的鞋底材料是一种聚氨酯，缩写为 TPU。它并不是一种罕见的原料，从家家户户都有的乐扣乐扣密封盒、女士文胸配件到气管、燃料管都有可能应用到 TPU。耐克改造 TPU
                    的难度在于这种材料在热塑的过程中相当灵活，如何建模，如何让机器自动适应不同鞋码的模型要求，成为解决问题的关键。<br></p>

                <p>Vapormax 的鞋底有复杂精细的形状，仔细观察的话，它们的形态就像那些用数字绘图软件制成的建筑物。线条的分布依据算法，而不是普通的几何机构。耐克鞋类设计副总裁 Andy Caine
                    认可了这个说法，Vapormax 是 3D 数字化处理的结果。“相比手绘设计然后做出模型，我们可以可以更快捕捉数据，更具体的测试某个点的压力值，迅速建模。”Andy Caine
                    说，这样的做法提高了解决问题的速度——即便如此，也耗费了 7 年的研发时间。</p>

                <p>在耐克波特兰的气垫制造厂里，我们见到了 TPU
                    的原料形态：它是一种没有气味的、半透明不规则小颗粒，有的细长有的扁，但总体看上去，就像堆在铁桶里的一大堆米。抓起一把，你会感觉到温热，但几乎没什么重量，把它们洒回桶里的时候，还会有一些颗粒附着在掌心里。</p>

                <p>这些小颗粒会被制成一种相貌普通的塑料板。但据耐克 AIR 制造创新部门的副总裁 Lailit Montiero 透露，制作 Vapormax 的塑料板本身有特殊的成分。因为 Vapormax
                    鞋底一体成型，从塑料板到最终的成品都在一条流水作业的机器组里完成，所以除了最终的上色之外，并不存在额外添加的工序。制作 Vapormax
                    鞋底的机器大过一辆校车，从热塑成型、边缘切割到充气，基本上都由机械手完成，只需要一个工人在一旁作业。Lailit Montiero
                    说，“最终机器会把氮气从极细的针孔里注入切割好的鞋底。”经过质检之后，这些鞋垫会被运至韩国，由那边的耐克工厂完成 Vapormax 的最后制造。</p>

                <p>Lailit Montiero 显然对整套机器的设计感到相当骄傲，“它要求相当精密的自动化制造，这就是为什么我们用了 140 个工程师，差不多 10 个博士的原因。”“你问它的价格？我不能说。700
                    万美金这个量级听着怎么样？”</p>

                <p>Vapormax 可能是自耐克 2012 年发布 Flyknit 技术以来最具技术突破性的产品，但是否会成为像后者一样应用广泛、受众广泛、盈利能力同样广泛的产品还未可知。耐克公司依靠 Flyknit
                    技术攀上历史业绩的巅峰，因为 Flyknit 同样是从机器制造开始创新的技术，竞争对手复制同样的概念耗费了 3 年左右的时间。</p>

                <p>耐克需要新的故事。现任 CEO Mark Parker 已经确定了 2020 计划，即“2020 年完成 500 亿美元的年营收”。耐克 2016 年的营收为 323.76
                    亿美元，意味着要达成目标需要至少以每年 11.47% 的增速前进。Mark Parker 是耐克创始人 Phil Knight 之后的继位者，因为兼具商业思维和设计师思维而闻名业界，2015 年 6
                    月，耐克董事会奖励了 Mark Parker 价值 3000 万美元的公司股票，作为他至少在公司再待 5 年的激励。</p>

                <p>但耐克在 2016 年的表现不如人意。因为渠道萎缩和来自阿迪达斯、UA 的激烈竞争，耐克 2017 第二财季北美市场的期货订单下滑了 4%。Mark Parker
                    要求投资者注意耐克在全球市场的表现——它强调说耐克另外 50% 的生意发展依然顺利。</p>

                <p class="">但是来自潮流市场的声音也不可小觑，阿迪达斯正在占领年轻人越来越多的话题。一份来自球鞋爱好者 BigfootKix 的数据从“投资球鞋”的角度分析了 Jordan
                    为什么在过去一段时间失去了人气：假设一位 Sneaker 把钱都用来投资 Jordan 或投资 NMD，并将其与购买证券和购买耐克／阿迪达斯的股票对比，会发现如果他要做球鞋转卖的生意，如今买
                    Jordan 和耐克的股票都是亏的。</p>

