                <p>无论你何时见到 Vapormax，你都会立刻注意到它的鞋底。这基本上就是一个巨大的塑料气囊，只不过手感坚实，造型相当精细：鞋底的 5
                    个密集受力部位覆盖了耐磨损涂层，所有受力点都对应不规则、彼此相连的小气囊。看似前后脚掌一以贯之，但其实是两片气垫以极细密的接缝连在一起。</p>

                <p>Vapormax 看上去相当简单，传统球鞋至少 7 个部分的复杂构成被缩减到了 3 个：鞋底、鞋面和鞋带，各自一体成型。</p>

                <p>因为鞋底是个“泡泡”，大部分人看到 Vapormax 的第一反应都是：“它不会破吗？”</p>

