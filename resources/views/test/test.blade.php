                <p>
                    耐克一直想革新气垫技术。在以往的设计里，气垫只是被当作鞋底的一个部件，它的最大问题，是层层鞋底降低运动员脚掌的感知力，因此设计团队希望把解决问题的焦点放在“重新恢复感知力”上面。这促使他们用另一种眼光看待气垫本身——如果去掉包裹它的橡胶，直接让气垫接触地面会怎样？</p>

                <p>早在气垫技术开始不久，耐克其实有过相当类似的想法。在耐克展示 Vapormax 的会场上，关于气垫产品的历史沿革介绍也被详细列出，其中就有 Mark Parker 1981 年设计的全脚掌气垫以及被称为
                    Pillar Air 的原型鞋。在向媒体介绍 Vapormax 的房间里，John Hoke 把这只鞋和鲍尔曼 1970
                    年设计的华夫底鞋放在一起比较：气泡和橡胶，一个是透明的黄色，一个是黑色，但都是点状分布的鞋底设计，历史仿佛在这里开了一个玩笑。</p>

                <div class="com-insert-images">
                    <figure style="margin:0;">
                        <img alt="" data-ratio="0.636667" data-format="jpeg" class="lazyload"
                             data-src="http://img.qdaily.com/uploads/20170220015901POuSxZfXya3Ie2JR.jpeg-w600">
